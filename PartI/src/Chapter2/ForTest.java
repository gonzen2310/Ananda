package Chapter2;
/*
 * Demonstrate the for loop.
 * Call this file "ForTest.java"
 */
public class ForTest {
    public static void main(String[] args) {
        for (int x = 0; x < 10; x++) System.out.println("This is x: " + x);
    }
}

// For loop general form:
// for(initialization; condition; iteration) statement;
/*
 * INITIALIZATION: Sets a loop control variable to an initial value
 * CONDITION: Boolean expression that test the loop control variable
 * ITERATION: Expression that determines how the loop control variable is changed
 */
